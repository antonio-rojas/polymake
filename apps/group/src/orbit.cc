#include "polymake/client.h"
#include "polymake/group/orbit.h"

namespace polymake { namespace group { 

UserFunction4perl("# @category Orbits\n"
                  "# The orbit of a set //S// under a group generated by //G//."
                  "# @param Array<Array<Int>> G Generators of the group"
                  "# @param Set S"
                  "# @return Set",
                  (&orbit<Array<int>, Set<int> >), "orbit(Array<Array<Int>>, Set)");

UserFunction4perl("# @category Orbits\n"
                  "# The orbit of a set of sets //S// under a group generated by //G//."
                  "# @param Array<Array<Int>> G Generators of the group"
                  "# @param Set<Set> S"
                  "# @return Set",
                  (&orbit<Array<int>, Set<Set<int> > >), "orbit(Array<Array<Int>>, Set<Set>)");

UserFunction4perl("# @category Orbits\n"
                  "# The orbit of a vector //V// under a group generated by //G//."
                  "# @param Array<Matrix<Rational>> G Generators of the group"
                  "# @param Vector V"
                  "# @return Set",
                  (&orbit<Matrix<Rational>, Vector<Rational> >), "orbit(Array<Matrix>, Vector)");

} } // end namespaces
